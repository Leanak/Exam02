#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;

	if (ac == 2)
	{
		while (*av[1])
		{
			if (*av[1] >= 'a' && *av[1] <= 'z')
			{
				j = *av[1] - 96;
				while (j-- > 0)
					write(1, av[1], 1);
			}
			else if (*av[1] >= 'A' && *av[1] <= 'Z')
			{
				j = *av[1] - 64;
				while (j-- > 0)
					write(1, av[1], 1);
			}
			else
				write(1, av[1], 1);
			av[1]++;
		}	
	}
}