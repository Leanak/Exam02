#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;

	if (ac == 2)
	{
		while (*av[1])
		{
			if (*av[1] >= 'a' && *av[1] <= 'z')
			{	
				j = *av[1] - 96;
				*av[1] =  123 - j;
			}
			else if (*av[1] >= 'A' && *av[1] <= 'Z')
			{
				j = *av[1] - 64;
				*av[1] = 91 - j;
			}
			write(1, av[1]++, 1);
		}
	}
	write(1, "\n", 1);
}