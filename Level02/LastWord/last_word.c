#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	end;

	if (ac == 2)
	{
		i = 0;
		end = 0;
		while (av[1][i])
			i++;
		i -= 1;
		while ((av[1][i] == '\t' || av[1][i] == 32) && i > 0)
			i--;
		end = i;
		while ((av[1][i] != '\t' && av[1][i] != 32) && i > 0)
			i--;
		while (i <= end)
		{
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}