#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	index;

	if (ac == 2)
	{
		i = 0;
		index = 0;
		while (av[1][i])
			i++;
		i--;
		while ((av[1][i] == 32 || av[1][i] == 9) && (i > 0))
			i--;
		index = i;
		while ((av[1][i] != 32 && av[1][i] != 9) && (i > 0))
			i--;
		while (i <= index)
		{
			write(1, &av[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}